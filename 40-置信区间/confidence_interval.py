# 问原始分布的均值落在112左右12的范围的概率？
############################################################
# 对原始分布进行如下采样
############################################################
sample_nums = 36
std_sample_actual = 40

############################################################
# 用抽样分布的标准误差作为原始分布标准误差的无偏估计
############################################################
std_ori = std_sample_actual

############################################################
# 样本均值的抽样分布服从正态分布，标准差可以通过原始分布标准差计算
############################################################
std_sample_theory = std_ori / (sample_nums ** 0.5)

############################################################
# 根据正态分布，计算Z分数，再计算相应的概率
############################################################
z_score = 12 / std_sample_theory
# 查表可得Z分数小于z_score(1.8)的概率是0.9641
p = (0.9641 - 0.5) * 2